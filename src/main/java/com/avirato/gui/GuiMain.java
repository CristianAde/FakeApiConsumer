    private void jButtonSendEditCostumerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSendEditCostumerActionPerformed

        Customer customer;
        
        int id;
        String name = jTextFieldName.getText();
        String phone = jTextFieldPhone.getText();
        String email = jTextFieldEmail.getText();
        String birthDate = jTextFieldBirthDate.getText();
        String operacion = jLabelCustomerEdit.getText();
        
        
        
    switch (operacion) {

            case "Crear":
                customer = new Customer(name, phone, email, birthDate);
                jTextFieldMessages.setText(guiMainController.createCustomer(customer));
                populateCustomerTable();
                clearMessageBox();
                break;

            case "Modificar":
                id = Integer.parseInt(jTextFieldId.getText());
                customer = new Customer(id, name, phone, email, birthDate);
                jTextFieldMessages.setText(guiMainController.modifyCustomer(customer));
                populateCustomerTable();
                clearMessageBox();
                break;

            case "Eliminar":
                id = Integer.parseInt(jTextFieldId.getText());
                customer = new Customer(id, name, phone, email, birthDate);
                int confirmacion = JOptionPane.showConfirmDialog(null, "Desea borrar el cliente");
                if(confirmacion==0){
                    jTextFieldMessages.setText(guiMainController.deleteCustomer(customer));
                populateCustomerTable();
                } else {
                    jTextFieldMessages.setText("No se elimin√≥ al cliente");  
                }
                clearMessageBox();
                break;

            case "Busqueda por Id":
                try {
                    id = Integer.parseInt(jTextFieldId.getText());
                    customer = new Customer(id, name, phone, email, birthDate);
                    jTableDefaultModel = (DefaultTableModel) jTableCustomer.getModel();
                    jTableDefaultModel.setRowCount(0);
                    jTableDefaultModel.addRow(guiMainController.searchCustomerById(customer));
                    jTextFieldId.setEditable(false);
                } catch (Exception e) {
                    jTextFieldMessages.setText("Debe introducir un numero entre 1 y " + jTableDefaultModel.getRowCount());
                    clearMessageBox();
                }
            clearMessageBox();
            break;
        }
        clearCustomerEdit();
        disableCustomerEdit();

    }//GEN-LAST:event_jButtonSendEditCostumerActionPerformed

